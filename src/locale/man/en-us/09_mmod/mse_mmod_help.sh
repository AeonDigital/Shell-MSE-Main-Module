# @desc
#
# O myShellEnv é um módulo gerenciador de recursos multipropósitos para uso
# em terminais de linha de comando (Shell). Compatível principalmente com o
# Bash.
#
#
#
# @ Módulo Principal
#
# O módulo principal do MSE traz consigo uma série de ferramentas que oferecem
# tratamentos comuns de problemas que (ao menos eu) tive com frequencia ao
# longo do tempo codificando aplicações seja em linha de comando ou em outros
# ambientes.
#
# As funções oferecidas estão divididas em submódulos (apenas para fins
# organizacionais)
#
# Para ter uma visão geral do todo do que está disponível use o comando:
#
# > mse_mmod_showMetaData 1
#
#
# OU, se quiser pesquisar por algum objeto em especial, use:
#
# > mse_mmod_showMetaData 1 1 [[modulo]] [[submodulo]] [[função]]
# Ex:
# > mse_mmod_showMetaData 1 1 "" "" show
#
# O exemplo acima irá mostrar todas as funções de quaisquer módulos e
# submódulos que tenham "show" em seus nomes.
#
#
#
# @ Manual das Funções
#
# Para conhecer melhor uma função e entender como ela deve ser usada você
# pode usar o comando:
#
# > mse man [[função]]
#
# Isto mostrará na tela toda a descrição daquela função contendo suas regras
# de uso e parametros esperados.
#
